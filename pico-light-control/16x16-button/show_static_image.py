DATA_PIN = 0
WIDTH = 16
HEIGHT = 16
NUM_LEDS = WIDTH * HEIGHT
BRIGHTNESS = 0.25
SERPENTINE_LAYOUT = True
FLIP_VERTICAL = False

np = neopixel.NeoPixel(Pin(DATA_PIN), NUM_LEDS)


def scale_color(color, amount):
    return (
        int(color[0] * amount),
        int(color[1] * amount),
        int(color[2] * amount),
    )


def matrix_index(x, y):
    if FLIP_VERTICAL:
        y = HEIGHT - 1 - y

    if SERPENTINE_LAYOUT and y % 2 == 1:
        x = WIDTH - 1 - x

    return y * WIDTH + x


def clear():
    for index in range(NUM_LEDS):
        np[index] = (0, 0, 0)
    np.write()


def show_image(pixel_rows):
    for y in range(HEIGHT):
        for x in range(WIDTH):
            color = pixel_rows[y][x]
            np[matrix_index(x, y)] = scale_color(color, BRIGHTNESS)
    np.write()


def main():
    clear()
    show_image(pixels)


main()
